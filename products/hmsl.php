<h2 class="title">HLR / mobile status lookup</h2>
<p>Query the Home Location Register databases of mobile network operators.<br><br>


To correctly identify the active or inactive status of a mobile telephone number. More than 1000 networks provide access to their active status via the E164.com portal. <br><br>

HLR dataset queries are available via the online portal, file uploads, managed service and API.</p>

