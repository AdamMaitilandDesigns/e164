<h2 class="title">STIR / SHAKEN</h2>

                <p>Integrate the E164.com STIR/SHAKEN database to authenticate origination and termination telephone numbers.<br><br>



Using the STIR mechanism, transit and terminating providers can ensure that provided CLI and presentation telephone numbers are accurate and belong to the known list of IDs authorised upstream for the originating customer.<br><br>


Query SHAKEN information for a telephone number to understand how to deal with a phone number that has failed or omitted STIR verification.<br><br>



By conforming to the STIR/SHAKEN suite of protocols via E164.com, customers can reduce robocalls and become compliant with the USA Federal Communications Commission guidelines and the Canadian Radio-Television and Telecommunications Commission requirements.</p>

