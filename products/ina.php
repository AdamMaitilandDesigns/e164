<h2 class="title">Inbound Number allocation / DID</h2>
<p>Assign new inbound telephone numbers to receive inbound telephone calls or SMS messages.<br><br>


To provision a new inbound telephone number from global partners, use the E164.com portal or API and set SMPP or SIP routing details to route inbound calls and SMS to your service.

Inbound numbering can be transferred between accounts allocated to an organisation.<br><br>


E164.com will arrange for telephone calls and SMS to be carried to your inbound node or call centre with minimum network lag using the E164.com optimised routing system.</p>

