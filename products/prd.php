<h2 class="title">Premium rate detection</h2>
<p>Using the E164.com dialplan provides a mechanism to instantly detect international premium rate telephone numbers before they are a problem for your company.<br><br>


Different territories implement premium rate telephone numbers in different ways.<br><br>



Premium rate detection includes international revenue share, local revenue share, per-minute billing, drop calls, and telephone numbers typically used for call-back missed call fraud (referred to as wangiri).</p>

