<h2 class="title">Do not call</h2>
<p>The national Do Not Call schemes regulate domestic markets to ensure call centres do not make unsolicited telephone calls to users who have specifically requested not to be contacted.<br><br>



E164.com acts as the coordinating system for different DNC list operators and provides a unified access to multiple market data via one interface.<br><br>


Access the Do Not Call list for multiple countries and details of adding telephone numbers to the local Do Not Call registry.</p>

